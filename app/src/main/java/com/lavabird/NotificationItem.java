package com.lavabird;

public class NotificationItem {
    String name_of_app;
    String notification_text;
    String time_of_notification;
    String date_of_notification;
    String notification_image;

    public String getName_of_app() {
        return name_of_app;
    }

    public void setName_of_app(String name_of_app) {
        this.name_of_app = name_of_app;
    }

    public String getNotification_text() {
        return notification_text;
    }

    public void setNotification_text(String notification_text) {
        this.notification_text = notification_text;
    }

    public String getTime_of_notification() {
        return time_of_notification;
    }

    public void setTime_of_notification(String time_of_notification) {
        this.time_of_notification = time_of_notification;
    }

    public String getDate_of_notification() {
        return date_of_notification;
    }

    public void setDate_of_notification(String date_of_notification) {
        this.date_of_notification = date_of_notification;
    }

    public String getNotification_image() {
        return notification_image;
    }

    public void setNotification_image(String notification_image) {
        this.notification_image = notification_image;
    }
}
